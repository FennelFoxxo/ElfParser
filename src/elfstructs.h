#pragma once

#define IDENT_MAG0  0x7f
#define IDENT_MAG1  'E'
#define IDENT_MAG2  'L'
#define IDENT_MAG3  'F'

#define EI_NIDENT  16

typedef struct __attribute__((packed, aligned(EI_NIDENT))) {
    uint8_t ei_mag0;
    uint8_t ei_mag1;
    uint8_t ei_mag2;
    uint8_t ei_mag3;
    uint8_t ei_class;
    uint8_t ei_data;
    uint8_t ei_version;
    uint8_t ei_osabi;
    uint8_t ei_abiversion;
} Elf_Ident;

typedef struct __attribute__((packed)) {
    uint8_t     e_ident[EI_NIDENT];
    uint16_t    e_type;
    uint16_t    e_machine;
    uint32_t    e_version;
    uint32_t    e_entry;
    uint32_t    e_phoff;
    uint32_t    e_shoff;
    uint32_t    e_flags;
    uint16_t    e_ehsize;
    uint16_t    e_phentsize;
    uint16_t    e_phnum;
    uint16_t    e_shentsize;
    uint16_t    e_shnum;
    uint16_t    e_shstrndx;
} Elf32_Ehdr;

typedef struct __attribute__((packed)) {
    uint8_t     e_ident[EI_NIDENT];
    uint16_t    e_type;
    uint16_t    e_machine;
    uint32_t    e_version;
    uint64_t    e_entry;
    uint64_t    e_phoff;
    uint64_t    e_shoff;
    uint32_t    e_flags;
    uint16_t    e_ehsize;
    uint16_t    e_phentsize;
    uint16_t    e_phnum;
    uint16_t    e_shentsize;
    uint16_t    e_shnum;
    uint16_t    e_shstrndx;
} Elf64_Ehdr;

typedef struct __attribute__((packed)) {
	uint32_t	sh_name;
	uint32_t	sh_type;
	uint32_t	sh_flags;
	uint32_t	sh_addr;
	uint32_t	sh_offset;
	uint32_t	sh_size;
	uint32_t	sh_link;
	uint32_t	sh_info;
	uint32_t	sh_addralign;
	uint32_t	sh_entsize;
} Elf32_Shdr;

typedef struct __attribute__((packed)) {
	uint32_t	sh_name;
	uint32_t	sh_type;
	uint64_t	sh_flags;
	uint64_t	sh_addr;
	uint64_t	sh_offset;
	uint64_t	sh_size;
	uint32_t	sh_link;
	uint32_t	sh_info;
	uint64_t	sh_addralign;
	uint64_t	sh_entsize;
} Elf64_Shdr;

typedef struct __attribute__((packed)) {
	uint32_t    st_name;
	uint32_t	st_value;
	uint32_t	st_size;
	uint8_t	    st_info;
	uint8_t	    st_other;
	uint16_t	st_shndx;
} Elf32_Sym;

typedef struct __attribute__((packed)) {
	uint32_t	st_name;
	uint8_t	    st_info;
	uint8_t	    st_other;
	uint16_t	st_shndx;
	uint64_t	st_value;
	uint64_t    st_size;
} Elf64_Sym;

typedef struct __attribute__((packed)) {
	uint32_t	p_type;
	uint32_t	p_offset;
	uint32_t	p_vaddr;
	uint32_t	p_paddr;
	uint32_t	p_filesz;
	uint32_t	p_memsz;
	uint32_t	p_flags;
	uint32_t	p_align;
} Elf32_Phdr;

typedef struct __attribute__((packed)) {
	uint32_t    p_type;
	uint32_t	p_flags;
	uint64_t	p_offset;
	uint64_t	p_vaddr;
	uint64_t	p_paddr;
	uint64_t	p_filesz;
	uint64_t	p_memsz;
	uint64_t	p_align;
} Elf64_Phdr;
